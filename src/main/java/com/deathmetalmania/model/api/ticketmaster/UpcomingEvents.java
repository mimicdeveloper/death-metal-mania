
package com.deathmetalmania.model.api.ticketmaster;




public class UpcomingEvents {

    private Integer ticketweb;
    private Integer total;
    private Integer filtered;
    private Integer tmr;
    private Integer ticketmaster;

    public Integer getTicketweb() {
        return ticketweb;
    }

    public void setTicketweb(Integer ticketweb) {
        this.ticketweb = ticketweb;
    }

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public Integer getFiltered() {
        return filtered;
    }

    public void setFiltered(Integer filtered) {
        this.filtered = filtered;
    }

    public Integer getTmr() {
        return tmr;
    }

    public void setTmr(Integer tmr) {
        this.tmr = tmr;
    }

    public Integer getTicketmaster() {
        return ticketmaster;
    }

    public void setTicketmaster(Integer ticketmaster) {
        this.ticketmaster = ticketmaster;
    }

}
